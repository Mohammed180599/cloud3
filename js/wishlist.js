function changeHeartIcon1()

{
    var heartIcon = document.getElementById('heartIcon1');
    if (heartIcon.src.includes('heart-default.png')) {
        heartIcon.src = 'assets/icons/heart-clicked.png'; 
    } else {
        heartIcon.src = 'assets/icons/heart-default.png';
    }
}

